def parsear_sentimiento(texto):
   texto = texto.lower().strip()
   matches = list(re.finditer(r"\b(positivo|negativo|neutro)\b", texto))
   if matches:
       return matches[-1].group(1)
   return "no_clasificado"

def generar_respuesta(model, tokenizer, prompt, max_new_tokens=50):
   inputs = tokenizer(
       prompt, return_tensors="pt", truncation=True, max_length=512
   ).to(device)
   with torch.no_grad():
       outputs = model.generate(
           **inputs,
           max_new_tokens=max_new_tokens,
           do_sample=False,
           pad_token_id=tokenizer.pad_token_id,
       )
   return tokenizer.decode(
       outputs[0][inputs["input_ids"].shape[1]:], skip_special_tokens=True
   )

def evaluar_tecnica(model, tokenizer, textos, labels_true, tecnica_nombre, prompt_fn):
   predicciones = []
   detalles = []
   for i, resena in enumerate(textos):
       if tecnica_nombre == "iterative":
           p1 = prompt_iterative_fase1(resena)
           r1 = generar_respuesta(model, tokenizer, p1)
           clasif1 = parsear_sentimiento(r1)
           p2 = prompt_iterative_fase2(resena, clasif1)
           r2 = generar_respuesta(model, tokenizer, p2)
           pred = parsear_sentimiento(r2)
           detalles.append({
               "resena": resena, "prompt_fase1": p1, "respuesta_fase1": r1,
               "clasificacion_fase1": clasif1, "prompt_fase2": p2,
               "respuesta_fase2": r2, "prediccion_final": pred,
           })
       elif tecnica_nombre == "cot":
           p = prompt_fn(resena)
           resp = generar_respuesta(model, tokenizer, p, max_new_tokens=150)
           pred = parsear_sentimiento(resp)
           detalles.append({"resena": resena, "prompt": p, "respuesta_cruda": resp, "prediccion": pred})
       else:
           p = prompt_fn(resena)
           resp = generar_respuesta(model, tokenizer, p)
           pred = parsear_sentimiento(resp)
           detalles.append({"resena": resena, "prompt": p, "respuesta_cruda": resp, "prediccion": pred})
       predicciones.append(pred)
       if (i + 1) % 50 == 0:
           print(f"    {tecnica_nombre}: {i + 1}/{len(textos)} reseñas procesadas")
   return predicciones, detalles

def calcular_metricas(y_true, y_pred):
   clases = ["positivo", "negativo", "neutro"]
   no_clasif = sum(1 for p in y_pred if p not in CLASES_VALIDAS)
   y_pred_eval = [p if p in CLASES_VALIDAS else "neutro" for p in y_pred]
   acc = accuracy_score(y_true, y_pred_eval)
   prec = precision_score(y_true, y_pred_eval, labels=clases, average=None, zero_division=0)
   rec = recall_score(y_true, y_pred_eval, labels=clases, average=None, zero_division=0)
   f1 = f1_score(y_true, y_pred_eval, labels=clases, average=None, zero_division=0)
   f1_macro = f1_score(y_true, y_pred_eval, labels=clases, average="macro", zero_division=0)
   cm = confusion_matrix(y_true, y_pred_eval, labels=clases)
   return {
       "accuracy": round(acc, 4),
       "precision": {c: round(p, 4) for c, p in zip(clases, prec)},
       "recall": {c: round(r, 4) for c, r in zip(clases, rec)},
       "f1": {c: round(f, 4) for c, f in zip(clases, f1)},
       "f1_macro": round(f1_macro, 4),
       "confusion_matrix": cm.tolist(),
       "no_clasificado": no_clasif,
   }

def bootstrap_f1_macro(y_true, y_pred, n_bootstrap=1000, ci=0.95):
   clases = ["positivo", "negativo", "neutro"]
   y_true, y_pred = np.array(y_true), np.array(y_pred)
   np.random.seed(42)
   f1_scores = []
   for _ in range(n_bootstrap):
       idx = np.random.choice(len(y_true), size=len(y_true), replace=True)
       f1_scores.append(f1_score(y_true[idx], y_pred[idx], labels=clases, average="macro", zero_division=0))
   lower = np.percentile(f1_scores, (1 - ci) / 2 * 100)
   upper = np.percentile(f1_scores, (1 + ci) / 2 * 100)
   return round(lower, 4), round(upper, 4)

