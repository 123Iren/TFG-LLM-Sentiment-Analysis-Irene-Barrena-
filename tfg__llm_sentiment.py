def medir_tamano_modelo(model_name):
   from huggingface_hub import model_info
   try:
       info = model_info(model_name)
       total = 0
       if info.siblings:
           for sibling in info.siblings:
               if sibling.rfilename.endswith((".safetensors", ".bin")):
                   if sibling.size is not None:
                       total += sibling.size
       if total > 0:
           return total / 1e9
       if hasattr(info, "safetensors") and info.safetensors:
           params = info.safetensors.get("total", 0)
           return (params * 2) / 1e9
   except Exception as e:
       print(f"  ⚠️ No se pudo obtener tamaño via API: {e}")
   print("  Usando estimación por defecto")
   return 0.0


def medir_ram_vram():
   ram = psutil.Process(os.getpid()).memory_info().rss / 1e9
   vram = torch.cuda.memory_allocated() / 1e9 if torch.cuda.is_available() else 0
   vram_peak = torch.cuda.max_memory_allocated() / 1e9 if torch.cuda.is_available() else 0
   return {"ram_gb": round(ram, 2), "vram_gb": round(vram, 2), "vram_peak_gb": round(vram_peak, 2)}


def medir_latencia(model, tokenizer, resenas, max_new_tokens=30):
   tiempos = []
   tokens_generados = []
   for i, resena in enumerate(resenas):
       prompt = (
           "Clasifica el sentimiento de la siguiente reseña como positivo, "
           "negativo o neutro. Responde únicamente con una palabra.\n\n"
           f"Reseña: {resena}\n\nSentimiento:"
       )
       inputs = tokenizer(
           prompt, return_tensors="pt", truncation=True, max_length=512
       ).to(device)
       if torch.cuda.is_available():
           torch.cuda.synchronize()
       t0 = time.time()
       with torch.no_grad():
           outputs = model.generate(
               **inputs,
               max_new_tokens=max_new_tokens,
               do_sample=False,
               pad_token_id=tokenizer.pad_token_id,
           )
       if torch.cuda.is_available():
           torch.cuda.synchronize()
       t1 = time.time()
       n_tokens = outputs.shape[1] - inputs["input_ids"].shape[1]
       tiempos.append(t1 - t0)
       tokens_generados.append(n_tokens)
       if (i + 1) % 10 == 0:
           print(f"  Procesadas {i + 1}/{len(resenas)} reseñas...")
   tiempo_total = sum(tiempos)
   total_tokens = sum(tokens_generados)
   return {
       "tiempo_medio_por_resena_s": round(np.mean(tiempos), 3),
       "tokens_por_segundo": round(total_tokens / tiempo_total, 2) if tiempo_total > 0 else 0,
       "tiempo_total_s": round(tiempo_total, 2),
       "resenas_procesadas": len(resenas),
   }


def medir_perplejidad(model, tokenizer, texto, max_length=512, stride=256):
   encodings = tokenizer(texto, return_tensors="pt", truncation=True, max_length=50000)
   input_ids = encodings.input_ids.to(device)
   seq_len = input_ids.size(1)
   print(f"  Tokens totales WikiText-2: {seq_len}")
   nlls = []
   n_chunks = 0
   for begin in range(0, seq_len, stride):
       end = min(begin + max_length, seq_len)
       input_chunk = input_ids[:, begin:end]
       target_chunk = input_chunk.clone()
       if begin > 0:
           overlap = begin + max_length - end
           if overlap > 0:
               target_chunk[:, :max_length - (end - begin)] = -100
       with torch.no_grad():
           outputs = model(input_chunk, labels=target_chunk)
           nll = outputs.loss
       if not torch.isnan(nll) and not torch.isinf(nll):
           nlls.append(nll.item())
       n_chunks += 1
       if end == seq_len:
           break
   if not nlls:
       print("  ⚠️ No se pudieron calcular NLLs válidos")
       return float("inf")
   ppl = np.exp(np.mean(nlls))
   print(f"  Chunks procesados: {n_chunks}, NLLs válidos: {len(nlls)}")
   return round(ppl, 2)


