def evaluar_modelo_completo(nombre, repo):
   print(f"\n{'='*70}")
   print(f"=== {nombre} ===")
   print(f"{'='*70}")
   limpiar_memoria()
   print("Cargando modelo...")
   tokenizer = AutoTokenizer.from_pretrained(repo, trust_remote_code=True)
   if tokenizer.pad_token is None:
       tokenizer.pad_token = tokenizer.eos_token
       tokenizer.pad_token_id = tokenizer.eos_token_id
   model = AutoModelForCausalLM.from_pretrained(
       repo, quantization_config=bnb_config, device_map="auto", trust_remote_code=True,
   )
   model.eval()
   print("Modelo cargado.\n")
   resultados_modelo = {}
   for tecnica_nombre, prompt_fn in TECNICAS.items():
       print(f"\n  --- Técnica: {tecnica_nombre} ---")
       t0 = time.time()
       predicciones, detalles = evaluar_tecnica(model, tokenizer, textos, labels_true, tecnica_nombre, prompt_fn)
       t1 = time.time()
       print(f"    Tiempo: {t1 - t0:.1f}s")
       pred_eval = [p if p in CLASES_VALIDAS else "neutro" for p in predicciones]
       metricas = calcular_metricas(labels_true, predicciones)
       ci_lower, ci_upper = bootstrap_f1_macro(labels_true, pred_eval)
       metricas["f1_macro_ci_95"] = [ci_lower, ci_upper]
       metricas["tiempo_total_s"] = round(t1 - t0, 1)
       resultados_modelo[tecnica_nombre] = {"metricas": metricas, "predicciones": predicciones, "detalles": detalles}
       print(f"    F1-macro: {metricas['f1_macro']:.4f} [{ci_lower:.4f}, {ci_upper:.4f}]")
       print(f"    Accuracy: {metricas['accuracy']:.4f}")
       print(f"    No clasificados: {metricas['no_clasificado']}")
   del model, tokenizer
   limpiar_memoria()
   return resultados_modelo

